import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

const SYSTEM_PROMPT = `You are NWR Chalak Mitra AI — an expert troubleshooting assistant for Indian Railway Loco Pilots and Assistant Loco Pilots.

Your role is to help crew members diagnose and fix locomotive faults quickly and safely during duty.

When given a fault code, error message, or symptom description:

1. **Identify the Problem** — State the likely fault clearly
2. **Probable Causes** — List 2-4 most likely causes
3. **Step-by-Step Solution** — Provide numbered steps to resolve the issue
4. **⚠️ Safety Precautions** — Always include safety warnings relevant to the fault
5. **Do's and Don'ts** — Quick reference for the crew
6. **Emergency Quick Fix** — If applicable, provide a temporary workaround to keep the train moving safely

Cover all common Indian Railway locomotive types: WAP7, WAP5, WAG9, WAG9H, WDG4, WDP4, WDG4G, WDP4D.

Always prioritize safety. If a fault is critical and the loco should not be moved, clearly state that.
Use simple, clear language. Include Hindi terms in parentheses where helpful for field crew.
Format your response with clear headings using markdown.`;

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response("ok", { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const response = await fetch(
      "https://ai.gateway.lovable.dev/v1/chat/completions",
      {
        method: "POST",
        headers: {
          Authorization: `Bearer ${LOVABLE_API_KEY}`,
          "Content-Type": "application/json",
        },
        body: JSON.stringify({
          model: "google/gemini-3-flash-preview",
          messages: [
            { role: "system", content: SYSTEM_PROMPT },
            ...messages,
          ],
          stream: true,
        }),
      }
    );

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: "Rate limit exceeded. Please try again in a moment." }),
          { status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: "AI credits exhausted. Please contact admin." }),
          { status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(
        JSON.stringify({ error: "AI service temporarily unavailable" }),
        { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("troubleshoot error:", e);
    return new Response(
      JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }),
      { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  }
});
